#[derive(Debug, Clone, Copy, Serialize)]
pub struct ModelSpec {
    pub provider: ProviderKind,
    pub id: &'static str,
    pub tier: &'static str,
    pub status: &'static str,
    pub is_default: bool,
    pub context_tokens: Option<u64>,
    pub max_output_tokens: Option<u64>,
    pub thinking: &'static str,
    pub reasoning_levels: &'static [&'static str],
    pub cache: &'static str,
    pub tool_use: &'static str,
    pub structured_output: &'static str,
    pub recommended_for: &'static [&'static str],
    pub cli_params: &'static [&'static str],
    pub request_notes: &'static [&'static str],
    pub source: &'static str,
}

#[derive(Debug, Clone, Copy, Serialize)]
pub struct ProviderParamSpec {
    pub provider: ProviderKind,
    pub thinking_mapping: &'static str,
    pub reasoning_effort_mapping: &'static str,
    pub max_tokens_mapping: &'static str,
    pub cache_mapping: &'static str,
    pub supported_cli_params: &'static [&'static str],
    pub passthrough: &'static str,
}

