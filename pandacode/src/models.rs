pub fn builtin_models(provider: ProviderKind) -> impl Iterator<Item = &'static ModelSpec> {
    BUILTIN_MODELS
        .iter()
        .filter(move |model| model.provider == provider)
}

pub fn builtin_model(provider: ProviderKind, id: &str) -> Option<&'static ModelSpec> {
    BUILTIN_MODELS
        .iter()
        .find(|model| model.provider == provider && model.id.eq_ignore_ascii_case(id))
}

pub fn provider_for_model(id: &str) -> Option<ProviderKind> {
    BUILTIN_MODELS
        .iter()
        .find(|model| model.id.eq_ignore_ascii_case(id))
        .map(|model| model.provider)
}

pub fn builtin_models_for<'a>(
    providers: &'a [ProviderKind],
) -> impl Iterator<Item = &'static ModelSpec> + 'a {
    BUILTIN_MODELS
        .iter()
        .filter(move |model| providers.contains(&model.provider))
}

